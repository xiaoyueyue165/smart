const mogoose = require('mongoose');

const PostSchema = mogoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mogoose.model('Posts', PostSchema);
